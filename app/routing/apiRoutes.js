// ===============================================================================
// LOAD DATA

// ===============================================================================

var user = require('../data/friends');

// ===============================================================================
// ROUTING
// ===============================================================================

module.exports = function(app) {
  // API GET Requests
  //
  // ---------------------------------------------------------------------------

  app.get('/api/friends', function(req, res) {
    res.json(user);
  });

  // API POST Requests

  // ---------------------------------------------------------------------------

  app.post('/api/friends', function(req, res) {


    user.push(req.body);
    res.json(true);
  });

};
